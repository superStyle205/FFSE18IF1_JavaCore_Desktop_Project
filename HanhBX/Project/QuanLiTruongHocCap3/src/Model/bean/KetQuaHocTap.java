package Model.bean;

public class KetQuaHocTap {
	private String soThuTu;
	private String maHS;
	private String tenHS;
	private String diemTongKet;
	private String xepLoai;
	private String hanhKiem;
	private String ketQua;
	
	
	
	
	
	
	
	public KetQuaHocTap() {
		super();
		
	}
	public KetQuaHocTap(String soThuTu, String maHS, String tenHS, String diemTongKet, String xepLoai, String hanhKiem,
			String ketQua) {
		super();
		this.soThuTu = soThuTu;
		this.maHS = maHS;
		this.tenHS = tenHS;
		this.diemTongKet = diemTongKet;
		this.xepLoai = xepLoai;
		this.hanhKiem = hanhKiem;
		this.ketQua = ketQua;
	}
	public String getSoThuTu() {
		return soThuTu;
	}
	public void setSoThuTu(String soThuTu) {
		this.soThuTu = soThuTu;
	}
	public String getMaHS() {
		return maHS;
	}
	public void setMaHS(String maHS) {
		this.maHS = maHS;
	}
	public String getTenHS() {
		return tenHS;
	}
	public void setTenHS(String tenHS) {
		this.tenHS = tenHS;
	}
	public String getDiemTongKet() {
		return diemTongKet;
	}
	public void setDiemTongKet(String diemTongKet) {
		this.diemTongKet = diemTongKet;
	}
	public String getXepLoai() {
		return xepLoai;
	}
	public void setXepLoai(String xepLoai) {
		this.xepLoai = xepLoai;
	}
	public String getHanhKiem() {
		return hanhKiem;
	}
	public void setHanhKiem(String hanhKiem) {
		this.hanhKiem = hanhKiem;
	}
	public String getKetQua() {
		return ketQua;
	}
	public void setKetQua(String ketQua) {
		this.ketQua = ketQua;
	}
	
	
	
}
