package dao;

public class Lop {

	private String idLop;
	private String tenLop;

	public Lop() {

	}

	public Lop(String idLop, String tenLop) {

		this.idLop = idLop;
		this.tenLop = idLop;
	}

	public String getIdLop() {
		return idLop;
	}

	public void setIdLop(String idLop) {
		this.idLop = idLop;
	}

	public String getTenLop() {
		return tenLop;
	}

	public void setTenLop(String tenLop) {
		this.tenLop = tenLop;
	}

}
